            <!-- ヘッダー -->
            <div class="mb-8">
                <div class="md:flex md:items-center md:justify-between">
                    <div class="min-w-0 flex-1">
                        <h1 class="text-2xl font-bold leading-7 text-gray-900 sm:truncate sm:text-3xl sm:tracking-tight">
                            マイランキング
                        </h1>
                        <p class="mt-1 text-sm text-gray-700">あなたの個人的な店舗ランキングを作成・管理できます</p>
                    </div>
                    <div class="mt-4 flex space-x-3 md:ml-4 md:mt-0">
                        <NuxtLink to="/rankings/public" class="btn-secondary flex items-center">
                            <svg class="w-4 h-4 mr-2 fill-none" stroke="currentColor" viewBox="0 0 24 24">
                                <path
                                    stroke-linecap="round"
                                    stroke-linejoin="round"
                                    stroke-width="2"
                                    d="M15 12a3 3 0 11-6 0 3 3 0 016 0z"></path>
                                <path
                                    stroke-linecap="round"
                                    stroke-linejoin="round"
                                    stroke-width="2"
                                    d="M2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z"></path>
                            </svg>
                            みんなのランキングを見る
                        </NuxtLink>
                        <NuxtLink to="/rankings/create" class="btn-primary flex items-center">
                            <svg class="w-4 h-4 mr-2 fill-none" stroke="currentColor" viewBox="0 0 24 24">
                                <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 4v16m8-8H4"></path>
                            </svg>
                            ランキングを作成
                        </NuxtLink>
                    </div>
                </div>
            </div>

            <!-- フィルター -->
            <div class="mb-6">
                <div class="grid grid-cols-2 gap-4 max-w-md">
                    <!-- カテゴリフィルター -->
                    <div>
                        <select v-model="selectedCategory" @change="handleFilter" class="input-field">
                            <option value="">全てのカテゴリ</option>
                            <option v-for="category in categories" :key="category.id" :value="category.id">
                                {{ category.name }}
                            </option>
                        </select>
                    </div>

                    <!-- 公開状態フィルター -->
                    <div>
                        <select v-model="selectedVisibility" @change="handleFilter" class="input-field">
                            <option value="">全ての状態</option>
                            <option value="public">公開</option>
                            <option value="private">非公開</option>
                        </select>
                    </div>
                </div>
            </div>

            <!-- ローディング -->
            <LoadingSpinner v-if="loading" />

            <!-- エラーメッセージ -->
            <AlertMessage v-if="error" type="error" :message="error" @close="error = ''" />

            <!-- ランキング一覧 -->
            <div v-if="!loading && rankings.length > 0" class="space-y-4 md:space-y-6">
                <div
                    v-for="ranking in rankings"
                    :key="ranking.id"
                    class="bg-white rounded-lg shadow hover:shadow-md transition-shadow duration-200">
                    <div class="p-4 md:p-6">
                        <!-- ヘッダー部分 -->
                        <div class="flex items-start justify-between mb-4">
                            <div class="flex-1 min-w-0">
                                <div class="flex items-center space-x-3">
                                    <h3 class="text-lg font-semibold text-gray-900">
                                        <NuxtLink :to="`/rankings/${ranking.id}`" class="hover:text-blue-600 transition-colors">
                                            {{ ranking.title }}
                                        </NuxtLink>
                                    </h3>

                                    <!-- 公開状態バッジ -->
                                    <span
                                        class="inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium"
                                        :class="{
                                            'bg-green-100 text-green-800': ranking.is_public,
                                            'bg-gray-100 text-gray-800': !ranking.is_public,
                                        }">
                                        {{ ranking.is_public ? '公開' : '非公開' }}
                                    </span>
                                </div>

                                <p v-if="ranking.description" class="text-sm text-gray-700 mt-2">
                                    {{ ranking.description }}
                                </p>

                                <div class="flex flex-wrap items-center space-x-4 mt-3">
                                    <div class="flex items-center text-sm text-gray-700">
                                        <svg class="w-4 h-4 mr-1 fill-none" stroke="currentColor" viewBox="0 0 24 24">
                                            <path
                                                stroke-linecap="round"
                                                stroke-linejoin="round"
                                                stroke-width="2"
                                                d="M7 7h.01M7 3h5c.512 0 1.024.195 1.414.586l7 7a2 2 0 010 2.828l-7 7a2 2 0 01-2.828 0l-7-7A1.994 1.994 0 013 12V7a4 4 0 014-4z"></path>
                                        </svg>
                                        {{ ranking.category?.name || '総合' }}
                                    </div>
                                    <div class="flex items-center text-sm text-gray-700">
                                        <svg class="w-4 h-4 mr-1 fill-none" stroke="currentColor" viewBox="0 0 24 24">
                                            <path
                                                stroke-linecap="round"
                                                stroke-linejoin="round"
                                                stroke-width="2"
                                                d="M19 21V5a2 2 0 00-2-2H7a2 2 0 00-2 2v16m14 0h2m-2 0h-4m-5 0H9m0 0H7m-2 0h2m0 0h4"></path>
                                        </svg>
                                        {{ ranking.shops_count || 0 }}店舗
                                    </div>
                                    <div class="flex items-center text-sm text-gray-700">
                                        <svg class="w-4 h-4 mr-1 fill-none" stroke="currentColor" viewBox="0 0 24 24">
                                            <path
                                                stroke-linecap="round"
                                                stroke-linejoin="round"
                                                stroke-width="2"
                                                d="M12 8v4l3 3m6-3a9 9 0 11-18 0 9 9 0 0118 0z"></path>
                                        </svg>
                                        {{ formatDate(ranking.updated_at) }}
                                    </div>
                                </div>
                            </div>
                        </div>

                        <!-- 上位店舗プレビュー -->
                        <div v-if="ranking.shops && ranking.shops.length > 0" class="mt-4 border-t border-gray-200">
                            <div class="divide-y divide-gray-200">
                                <div
                                    v-for="shop in ranking.shops.slice(0, 3)"
                                    :key="shop.id"
                                    class="flex items-center space-x-3 py-2">
                                    <!-- 順位 -->
                                    <div
                                        class="flex-shrink-0 w-7 h-7 rounded-full flex items-center justify-center text-sm font-bold"
                                        :class="{
                                            'bg-yellow-100 text-yellow-800': shop.rank_position === 1,
                                            'bg-gray-100 text-gray-800': shop.rank_position === 2,
                                            'bg-orange-100 text-orange-800': shop.rank_position === 3,
                                        }">
                                        {{ shop.rank_position }}
                                    </div>

                                    <!-- 店舗情報 -->
                                    <div class="flex-1 min-w-0">
                                        <p class="text-base md:text-sm font-medium text-gray-900 truncate">
                                            {{ shop.name }}
                                        </p>
                                        <p v-if="shop.comment" class="text-sm md:text-xs text-gray-700 mt-1">
                                            {{ shop.comment }}
                                        </p>
                                    </div>
                                </div>
                            </div>
                        </div>
                    </div>
                </div>

                <!-- ページネーション -->
                <div v-if="totalPages > 1" class="flex justify-center mt-8">
                    <PaginationComponent
                        :current-page="currentPage"
                        :total-pages="totalPages"
                        :total-items="totalItems"
                        :per-page="perPage"
                        @page-change="handlePageChange" />
                </div>
            </div>

            <!-- 空の状態 -->
            <div v-if="!loading && rankings.length === 0" class="text-center py-12">
                <svg class="mx-auto h-12 w-12 text-gray-400 fill-none" stroke="currentColor" viewBox="0 0 24 24">
                    <path
                        stroke-linecap="round"
                        stroke-linejoin="round"
                        stroke-width="2"
                        d="M9 19v-6a2 2 0 00-2-2H5a2 2 0 00-2 2v6a2 2 0 002 2h2a2 2 0 002-2zm0 0V9a2 2 0 012-2h2a2 2 0 012 2v10m-6 0a2 2 0 002 2h2a2 2 0 002-2m0 0V5a2 2 0 012-2h2a2 2 0 012 2v14a2 2 0 01-2 2h-2a2 2 0 01-2-2z"></path>
                </svg>
                <h3 class="mt-2 text-sm font-medium text-gray-900">ランキングがありません</h3>
                <p class="mt-1 text-sm text-gray-700">
                    {{
                        selectedCategory || selectedVisibility
                            ? 'フィルター条件に一致するランキングが見つかりませんでした。'
                            : '最初のランキングを作成してみましょう。'
                    }}
                </p>
                <div class="mt-6">
                    <NuxtLink to="/rankings/create" class="btn-primary flex items-center">
                        <svg class="w-4 h-4 mr-2 fill-none" stroke="currentColor" viewBox="0 0 24 24">
                            <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 4v16m8-8H4"></path>
                        </svg>
                        ランキングを作成
                    </NuxtLink>
                </div>
            </div>
        </div>
    </div>
</template>

<script setup lang="ts">
import type { Ranking, Category } from '~/types/api'

// 認証ミドルウェア適用
definePageMeta({
    middleware: 'auth',
})

const { $api } = useNuxtApp()

// リアクティブデータ
const rankings = ref<Ranking[]>([])
const categories = ref<Category[]>([])
const loading = ref(true)
const error = ref('')
const selectedCategory = ref('')
const selectedVisibility = ref('')

// ページネーション
const currentPage = ref(1)
const perPage = ref(20)
const totalItems = ref(0)
const totalPages = ref(0)

// フィルター
const handleFilter = () => {
    currentPage.value = 1 // フィルター変更時は1ページ目に戻る
    loadRankings()
}

// ページ変更
const handlePageChange = (page: number) => {
    currentPage.value = page
    loadRankings()
    window.scrollTo({ top: 0, behavior: 'smooth' })
}

// ランキングデータ取得
const loadRankings = async () => {
    try {
        loading.value = true

        const params: Record<string, string | number> = {
            page: currentPage.value,
            per_page: perPage.value,
        }
        if (selectedCategory.value) params.category_id = selectedCategory.value
        if (selectedVisibility.value) params.is_public = selectedVisibility.value === 'public' ? '1' : '0'

        const response = await $api.rankings.myRankings(params)

        // ページネーション対応のレスポンス処理
        rankings.value = response.data || []

        if (response.meta) {
            currentPage.value = response.meta.current_page
            perPage.value = response.meta.per_page
            totalItems.value = response.meta.total
            totalPages.value = response.meta.last_page
        }
    } catch (err) {
        console.error('Failed to load rankings:', err)
        error.value = 'ランキングデータの取得に失敗しました'
    } finally {
        loading.value = false
    }
}

// カテゴリデータ取得
const loadCategories = async () => {
    try {
        const response = await $api.categories.list()
        categories.value = response.data || []
    } catch (err) {
        console.error('Failed to load categories:', err)
    }
}

// ユーティリティ関数
const formatDate = (dateString: string) => {
    return new Date(dateString).toLocaleDateString('ja-JP')
}

// 初期化
onMounted(async () => {
    await Promise.all([loadRankings(), loadCategories()])
})

// メタデータ設定
useHead({
    title: 'ランキング管理 - マジキチメシ',
    meta: [{ name: 'description', content: '個人的な店舗ランキングの管理ページ' }],
})
</script>
